let n1 = prompt("Please enter first number")
let n2 = prompt("Please enter second number")

if (n1 > n2) {

    console.log(`${n1}`)
    document.write(`${n1}`);

} else if (n2 > n1) {

    console.log(`${n2}`)
    document.write(`${n2}`);

} else {

    document.write(`${n1} e ${n2} sono uguali`);

}